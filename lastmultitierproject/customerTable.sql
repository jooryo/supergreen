
CREATE TABLE customers(
   ID int  NOT NULL AUTO_INCREMENT PRIMARY KEY,
		username varchar(30),
   Fname varchar(30),
    Lname varchar(30),
	Sex varchar(20),
	Bday varchar(30),
	 email varchar(20),
   phone varchar(30),
  City varchar(30),
    Street varchar(30),
	  ZIPcode varchar(30),
	  password varchar(30)
   
)




